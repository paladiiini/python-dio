# atribuição simples
saldo = 500

print(saldo)

#atribuição com soma
saldo += 500

print(saldo)

#atribuição com subtração
saldo -= 200

print(saldo)

#atribuição com multiplicação
saldo *= 2

print(saldo)

#atribuição com divisão
saldo /= 2

print(saldo)

#atribuição com potência
saldo **= 2

print(saldo)

#atribuição com módulo
saldo %= 2

print(saldo)

#atribuição com expressões
saldo += (saldo * 0.1) + 100

print(saldo)